package Questions_level02;
//Task: Write a program to calculate the sum of the first 10 natural numbers (1 to 10).
public class SumOfFirstNnumbers {
    public static void main(String[] args) {
        int i = 0 ;
        int sum = 0;
        while(i < 10){
            sum = sum + i;
            System.out.println(sum);
            i++;


        }

    }
}
